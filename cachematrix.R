## Write a short comment describing this function
#makeCacheMatrix generate a special "matrix", which is really a list of functions
#including: set(y) - set the matrix
#           get() - get the matrix
#           set_inverse(inv) - set the inverse of the matrix
#           get_inverse() - get the inverse of the matrix
#The special "matrix" can access the matrix and its inverse matrix by using those functions
makeCacheMatrix <- function(x = matrix()) {
  inverse <- NULL
  set <- function(y) {
    x <<- y
    inverse <<- NULL
  }
  get <- function() x
  set_inverse <- function(inv) {
    inverse <<- inv
  }
  get_inverse <- function() inverse
  list(set = set, get = get,
       set_inverse = set_inverse,
       get_inverse = get_inverse)

}


