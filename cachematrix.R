#cacheSolve function return the inverse of the special "matrix" generated by makeCatheMatrix
#if the inverse has been calculated, then return the cached data
#otherwise, use solve function to calculate the inverse 
#and cache it by using the set_inverse() method in the special "matrix"
cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  inv <- x$get_inverse()
  if(is.null(inv)) {
    mat <- x$get()
    inv <- solve(mat, ...)
    x$set_inverse(inv)
    return(inv)
  } else {
    message('getting cached data')
    return(inv)
  }
}
